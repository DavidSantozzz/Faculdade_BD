USE dbTestePermissao;

CREATE TABLE DADOS_RESTRITOS ( ID INT IDENTITY( 1, 1 ) PRIMARY KEY,
									INFO VARCHAR ( 60 )
								 );

INSERT INTO DADOS_RESTRITOS ( INFO ) VALUES ( '* * * INFORMAÇĂO RESTRITA * * *' );

GO
CREATE PROCEDURE LER_DADOS_RESTRITOS
AS
BEGIN
	DECLARE @INFO VARCHAR ( 60 );
	SET @INFO= ( SELECT TOP 1 INFO FROM DADOS_RESTRITOS );
	PRINT( @INFO );
END;
GO

CREATE USER USUARIO_RESTRITO WITH PASSWORD = 'SENHA123';

PRINT( '---------------------------------------------' );
EXECUTE AS USER = 'USUARIO_RESTRITO';
PRINT( 'USUÁRIO TENTA ACESSO ANTES DA CONCESSĂO.' );

BEGIN TRY
	EXEC LER_DADOS_RESTRITOS;
	PRINT( 'USUÁRIO EXECUTOU A PROCEDURE ANTES DA CONCESSĂO.' );
END TRY
BEGIN CATCH
	PRINT( 'NĂO CONSEGUIU: PERMISSĂO AINDA NĂO CONCEDIDA.' )
END CATCH

REVERT;		-- REVERTE O CONTEXTO. RETORNANDO A SYSADMIN.

PRINT( '----------------------------------------------------' )
PRINT( 'AGORA A PERMISSĂO SERÁ CONCEDIDA AO USUÁRIO.' );

-- CONCEDE A PERMISSĂO Ŕ STORED PROCEDURE.
GRANT EXEC ON LER_DADOS_RESTRITOS TO USUARIO_RESTRITO;

EXECUTE AS USER = 'USUARIO_RESTRITO';
BEGIN TRY
	EXEC LER_DADOS_RESTRITOS;
	PRINT( 'USUÁRIO EXECUTOU APÓS RECEBER A CONCESSĂO.' );
END TRY
BEGIN CATCH
	PRINT( 'NĂO CONSEGUIU: MESMO COM A CONCESSĂO.' );
END CATCH;

REVERT;		-- REVERTE O USUÁRIO.

PRINT( '---------------------------------------------------' );
PRINT( 'AGORA A PERMISSĂO SERÁ REVOGADA DO USUÁRIO.' );

-- REVOGA A CONCESSĂO Ŕ STORED PROCEDURE.
REVOKE EXEC ON LER_DADOS_RESTRITOS TO USUARIO_RESTRITO;

EXECUTE AS USER = 'USUARIO_RESTRITO';
BEGIN TRY
	EXEC LER_DADOS_RESTRITOS;
	PRINT( 'USUÁRIO EXECUTOU ÁPÓS CONCESSĂO SER REVOGADA.' );
END TRY
BEGIN CATCH
	PRINT( 'NĂO CONSEGUIU: A CONCESSĂO FOI REVOGADA.' );
END CATCH

REVERT;		-- REVERTE O USUÁRIO.


